// Look up a port's coordinates by name (case-insensitive). Returns {lat, lng}
// or null if unknown. Strips common AIS suffixes like "Anch.", " Anchorage",
// and a trailing ", Country" so "Rotterdam Anch., Netherlands" still resolves.
export function portCoords(name) {
  if (!name) return null;
  let key = String(name).trim().toUpperCase();
  // Drop a trailing ", COUNTRY" and any "ANCH."/"ANCHORAGE"/"BUNKERING" tails.
  key = key.split(",")[0].trim();
  key = key.replace(/\s+(ANCH\.?|ANCHORAGE|BUNKERING.*|AREA.*)$/i, "").trim();
  const p = PORTS[key];
  return p ? { lng: p[0], lat: p[1] } : null;
}

// Resolve a port to {lat, lng} using its UN/LOCODE first (reliable, supplied by
// VesselFinder) and falling back to fuzzy name matching. Returns null if the
// port can't be located, in which case the caller draws no route leg.
import { locodeCoords } from "@/lib/locodes";
export function resolvePort(name, locode) {
  return locodeCoords(locode) || portCoords(name);
}
